package com.sakshi.covid.vaccination.model;

/** 
 * Represents the Live Vaccination Count
*/
public class Live {
	private long count;

	/**
	 * Gets the Live vaccination count.
	 * 
	 * @return a count representing total Live vaccination count
	 */
	public long getCount() {
		return count;
	}

	/**
	 * Sets the Live vaccination count.
	 * 
	 * @param count count containing total Live vaccination count
	 */
	public void setCount(long count) {
		this.count = count;
	}	
}
